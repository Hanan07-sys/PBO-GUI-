    private void teks_EmasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_teks_EmasKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!((Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)))) {
            evt.consume();

            getToolkit().beep();

            JOptionPane.showMessageDialog(null, "Hanya Boleh Diinputkan Angka");

        }
    }//GEN-LAST:event_teks_EmasKeyPressed

