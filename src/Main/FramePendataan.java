    private void btn_ProsesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ProsesMouseClicked
        // TODO add your handling code here:
        int penghasilan = Integer.parseInt(teks_Penghasilan.getText());
        int hargaEmas = Integer.parseInt(teks_Emas.getText());
        long nisab = 85 * hargaEmas;
        teks_Nisab.setText(nisab + "");
        if (penghasilan >= nisab) {
            double totalZakat = 2.5/100 * penghasilan;
            int total = (int)totalZakat;
            teks_Total.setText(total + "");
        } else {
            JOptionPane.showMessageDialog(null, "Nisab Belum Terpenuhi");
            teks_Total.setText(0 + "");
        }
    }//GEN-LAST:event_btn_ProsesMouseClicked

