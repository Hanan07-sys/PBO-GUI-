    private void btn_SimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_SimpanMouseClicked
        // TODO add your handling code here:
        int ktp = Integer.parseInt(teks_KTP.getText());
        String nama = teks_Nama.getText();
        String alamat = teks_Alamat.getText();
        int penghasilan = Integer.parseInt(teks_Penghasilan.getText());
        int hargaEmas = Integer.parseInt(teks_Emas.getText());
        String nisabstr = teks_Nisab.getText();
        String totalstr = teks_Nisab.getText();
        if (nisabstr.equals("") || totalstr.equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan Masukkan Data dan Proses");

        } else {
            int totalZakat = 0;
            int nisab = Integer.parseInt(nisabstr);
            totalZakat = Integer.parseInt(teks_Total.getText());
            try {
                String sql = "INSERT INTO tbl_zakat VALUES('" + ktp + "', '" + nama
                        + "', '" + cb_jenisKelamin.getSelectedItem() + "', '" + alamat + "', '" + penghasilan + "', '" + hargaEmas
                        + "', '" + nisab + "', '" + totalZakat
                        + "')";
                java.sql.Connection conn = (Connection) Koneksi.getConnection();
                java.sql.PreparedStatement ptsm = conn.prepareStatement(sql);
                ptsm.execute(sql);
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
                LoadData();
                kosongkan_form();

            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_SimpanMouseClicked

