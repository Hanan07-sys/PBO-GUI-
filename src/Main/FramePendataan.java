    void LoadData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.KTP");
        model.addColumn("Nama");
        model.addColumn("Jenis_Kelamin");
        model.addColumn("Alamat");
        model.addColumn("Penghasilan");
        model.addColumn("Harga_Emas/1Gr");
        model.addColumn("Nisab");
        model.addColumn("Total_Zakat");
        try {
            String sql = "SELECT * FROM tbl_zakat";
            Connection conn = Koneksi.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Object[] row = new Object[8];
                row[0] = rs.getString("KTP");
                row[1] = rs.getString("Nama");
                row[2] = rs.getString("Jenis_Kelamin");
                row[3] = rs.getString("Alamat");
                row[4] = rs.getString("Penghasilan");
                row[5] = rs.getString("Harga_Emas");
                row[6] = rs.getString("Nisab");
                row[7] = rs.getString("Total_Zakat");
                model.addRow(row);
            }
            tblZakat.setModel(model);

        } catch (SQLException e) {
            System.out.println("Koneksi gagal " + e.toString());
        }
    }

